public class upAndDown {
    public static void main(String[] args) {
        int number = 3;
        System.out.println(number);
        number ++;
        System.out.println(number);
        number --;
        System.out.println(number);

        System.out.println(number); // 3
        System.out.println(number++); // 3
        System.out.println(number); // 4
    }



}
